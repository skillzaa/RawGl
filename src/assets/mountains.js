import GlUtil from "../core/glUtil.js";
import AssetBuilder from "../assets/assetBuilder.js";
const gl = GlUtil.getGl("bilza");
const asset = new AssetBuilder(0, 50, 50, 50);
asset.setColorBackground(0.4, 0.2, 0.2);
asset.vertices.addTriangle(0, 0, 50, 0, 25, 100, 1, 1, 0);
asset.vertices.addTriangle(50, 0, 100, 0, 75, 90, 1, 1, 0);
asset.vertices.addTriangle(25, 0, 75, 0, 50, 60, 0, 0.7, 0.3);
asset.vertices.addTriangle(40, 60, 60, 60, 50, 100, 0, 0.7, 0.3);
const core = asset.getAsset(gl);
core.x = 10;
core.y = 25;
core.init(gl);
GlUtil.clear(gl, 0, 0, 1);
core.draw(gl);
console.log("core", core);
